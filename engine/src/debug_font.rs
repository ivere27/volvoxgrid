/// Width in pixels of a string at the given integer scale.
#[inline]
pub fn str_width(text: &str, scale: i32) -> i32 {
    text.len() as i32 * CELL_W * scale
}

/// Height in pixels of a single line at the given integer scale.
#[inline]
pub fn line_height(scale: i32) -> i32 {
    CELL_H * scale
}

/// Draw a single ASCII character.  Returns advance width.
pub fn draw_char<C: Canvas>(canvas: &mut C, x: i32, y: i32, ch: u8, color: u32, scale: i32) -> i32 {
    canvas.draw_bitmap_char(x, y, ch, color, scale)
}

/// Draw a string.  Returns total advance width.
pub fn draw_str<C: Canvas>(
    canvas: &mut C,
    x: i32,
    y: i32,
    text: &str,
    color: u32,
    scale: i32,
) -> i32 {
    let advance = CELL_W * scale;
    let mut cx = x;
    for &ch in text.as_bytes() {
        draw_char(canvas, cx, y, ch, color, scale);
        cx += advance;
    }
    cx - x
}

/// Draw a string right-aligned so its right edge is at `right_x`.
pub fn draw_str_right<C: Canvas>(
    canvas: &mut C,
    right_x: i32,
    y: i32,
    text: &str,
    color: u32,
    scale: i32,
) -> i32 {
    let w = str_width(text, scale);
    draw_str(canvas, right_x - w, y, text, color, scale)
}
